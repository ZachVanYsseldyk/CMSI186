class CelsiusToFarenheit {
	public static void main(String args[]) {
		float celsius = 24.3f;
		double conversion = 1.8 * celsius + 32;
		System.out.println("24.3 celsius into Farenheit? Glad you asked. According to my amazing coding and mathematic skills, 24.3 celsius in Farenheit is "
			 + (conversion) + "F.");
	}
}
